export type Locale = 'zh-CN' | 'ja-JP';

export const supportedLocales = ['zh-CN', 'ja-JP'] as const;
export const defaultLocale: Locale = 'zh-CN';
export const localeCookieName = '7ch_lang';

export const isLocale = (value: unknown): value is Locale =>
  value === 'zh-CN' || value === 'ja-JP';

const dictionaries = {
  'zh-CN': {
    ...resources['zh-CN'].translation,
    ...zhCN,
  },
  'ja-JP': {
    ...resources['ja-JP'].translation,
    ...jaJP,
  },
};

export const tServer = (key: string, locale: Locale = defaultLocale) => {
  const dictionary = dictionaries[locale] ?? dictionaries[defaultLocale];
  return dictionary[key as keyof typeof dictionary] ?? key;
};
